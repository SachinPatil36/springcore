package com.example.model;

public class EmployeeContact {
	
	 String  contact;
	 String gmail;
	
	

	public EmployeeContact(String contact, String gmail) {
		super();
		this.contact = contact;
		this.gmail = gmail;
	}



	@Override
	public String toString() {
		return "EmployeeContact [contact=" + contact + ", gmail=" + gmail + "]";
	}

	
	
	

	
	
	

}
